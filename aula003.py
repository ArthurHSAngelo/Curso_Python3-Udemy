"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte significa que o Python consegue interpretar o argumento que foi inserido de forma dinâmica, ja reconhecendo o tipo dele, Ex: str, int, bool
Tipos de tipagem = (Fraca ou Forte) (Dinâmica ou Estática)

str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples 
print('Arthur Henrique')
print(1, 'Arthur "Henrique"')

# Aspas duplas
print("Arthur Henrique")
print(2, "Arthur 'Henrique'")

# Escape
print("Arthur \"Henrique\"")

# r (expressões regulares)
print(r"Arthur \"Henrique\"")